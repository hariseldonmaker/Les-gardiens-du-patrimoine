      <p>
      <p>Le château d'Imryrr, surnommé "La cité qui rêve" est un joyau architectural. Malheureusement, en vieillissant, ses toitures se sont abimées. Les fonds récoltés lors de cette grande journée serviront à la réparation de ces toitures. <b>Le prix d'entrée minimum sera de 10€</b>, mais libre à vous de donner plus si vous voulez nous soutenir !</p>
      <p>Le château d'Imryrr a été bâti sur la plaine de Melniboné en 1612, et se présente comme un modèle d'audace architecturale, tout en conservant des caractéristiques typiques de cette époque. C'est surtout l'un des mieux conservés du département de l'Isère (38). Son exceptionnel cadre nous a offert par le passé certaines de nos plus belles représentations. Aujourd'hui, c'est lui qui a besoin d'un petit coup de pouce.</p> 
      <p>Notre spectacle fera renaître la grande bataille de 1642, lorsque les troupes du Duché de Savoie ont pris d'assaut la forteresse, place stratégique du Royaume de France dans les Alpes. La grande première aura lieu le <b>Dimanche 23 Mai 2021</b> et se déroulera de <b>14h30 à 16h</b>. Pour se remettre de nos émotions, il y aura ensuite un cocktail, et il sera possible de discuter avec les "chevaliers" qui auront participé au spectacle.</p>
      <box>
      <span><p><b>Attention ! Soyez à l'heure, car l'accès au château sera réservé aux seuls spectateurs, et les retardataires ne seront pas acceptés !</b></p></span> 
