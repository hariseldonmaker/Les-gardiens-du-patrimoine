      <p>
      <p>The castle of Imryrr, nicknamed "The dreaming city" is an architectural jewel. Unfortunately, as becoming old, its roofs have been damaged. The funds gained during this great day will benefit to the roofing repair.</p>
      <p>The castle of Imryrr has been built on the plain of Melnibone in 1612, and sees itself as a model of architectural audaciousness, while keeping typical caracteristics of this era. It is therefore one of the most preserved in the department of Isère (38). In the past, its exceptional frame has offered us some of our most beautiful demonstrations. Today, it's the castle itself which needs a little helping hand.</p> 
      <p>Our show wil give rebirth to the great battle of 1642, when the Savoie duchy troops had taken assault against the fortress, strategic place of the France kingdom in the Alps. The grand premiere will take place on the <b>Sunday 23th of May 2021</b> from <b>14:30 to 16:00</b>. To recover from our emotions, then there will be a cocktail reception, and it will be possible to discuss with the "knights" who will have participated in the show.</p>
