<div></div><div class="sqs-block spacer-block sqs-block-spacer sized vsize-1" data-aspect-ratio="4.4989775051124745" data-block-type="21" id="block-b246b232db8b55fb15bc"><div class="sqs-block-content">&nbsp;</div></div></div><div class="col sqs-col-6 span-6"><div class="sqs-block html-block sqs-block-html" data-block-type="2" id="block-4f3f7dcd582d9601b55d"><div class="sqs-block-content"><p class="" style="white-space:pre-wrap;">&nbsp; <p>If you have come this way, that's because we share the same interest in the french architectural legacy. We then are going to tell you more about ourselves and what we do. Our association was born in March 2001, at the initiative of 2 history buffs : Eric Melniboné and Mickael Mourcoque. The first one is an history teacher, who has written several books about the Renaissance castles, and the second one is a mason who has always loved old stones.</p>
<p>Being big childrend, they have always kept a fascination for castles, impressing buildings holding centennial secrets. During those 20 years, they have been able to build up a reliable and important network of craftsmen with whom they work on the restoration of these monuments, everywhere in France, but especially in the Rhône-Alpes area from where they come from.</p>    
<p>We now own some tens of workers throughout France, and also volunteers without whom we couldn't do so many things for the legacy. Convinced that history has to be alive in our modern world, we also organize events as often as possible, which serve as much to make money as to highlight the buildings.</p>
<p>So, we have had the opportunity to create diverse historical pageants, as well as school visits and concerts. With <b><i>Les gardiens du patrimoine</i></b>, past has a future !</p>
